// Combined database (default uses both)
export const WORD_DATABASE = { ...WORD_DATABASE_USA, ...WORD_DATABASE_INDIA };

export function getRandomCategory(database = WORD_DATABASE) {
  const categories = Object.keys(database);
  return categories[Math.floor(Math.random() * categories.length)];
}

export function getRandomWord(category, database = WORD_DATABASE) {
  const words = database[category];
  if (!words) return null;
  return words[Math.floor(Math.random() * words.length)];
}

export function getCategoryAndWord(database = WORD_DATABASE) {
  const category = getRandomCategory(database);
  const word = getRandomWord(category, database);
  return { category, word };
}
